package Modulo01.Aula04;

public class a4_2_estruturaDecisaoSwitch {

    public static void main(String[] args) {
        int idade = 5;

        switch (idade){
            case 1:
                System.out.println("Valor 1");
                break;
            case 5:
                System.out.println("Valor 1");
                break;
            case 10:
                System.out.println("Valor 1");
                break;
            default:
                System.out.printf("Valor defaut %d", idade);
                break;
        }
    }
}
